package Programs;

public class Arrays_Assignment {

	public static void main(String[] args) {
		
		
		for(int i=0;i<=9;i++) { // inner loop
			
			for(int j=0;j<=9;j++) { // outer loop
				
				System.out.print(i+""+j + " ");// remove ln for to print horizontally
			}
			
			System.out.println();// to generate the new line if the first inner loop is done
				
			
		}

	}

}

package Automation_Testing;

public class Data_Types {

	public static void main(String[] args) {
       
		// It represents the type of a data either it can be int, float, double, boolean, 
		//It specifies that diff sizes & values that can be stored in the variable
		
		//Byte size 1 byte, 8 bits
		
		byte b =12;
		b = 23;
		System.out.println(b);
		
		//short size = 2 bytes, 16 bits
		
		short s = 10;
		short s1 = 32767;
		System.out.println(s1);
		
		//int size 4 bytes, 32 bits
		int a =10;
		int i = 123;
		System.out.println(i);
		
		//long size 8 bytes, 64 bits 
		long l = 5234632765l;
		long l2 =23434;
		System.out.println(l2);
		
		//float size 4 bytes upto store 7 decimal digits 
		float f1 = 23.345f;
		float f2 =3.3256553f;
		System.out.println(f2);
		
		//double size 8 bytes, 64 bits upto store 15 decimal digits
		double d =65.4352462364632;
		double d1 = 0.3233;
		System.out.println(d1);
		
		//boolean size 1 bit
		boolean city = true;
		boolean country = false;
		System.out.println(city);
		
		//char size 2 bytes
		
		char c ='M';
		char c1 ='F';
		System.out.println(c1);
			
	}

}
